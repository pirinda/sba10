    @Override
    public DGuiField getFieldFirst() {
        DGuiField fieldFirst = null;

        for (DGuiField field : moFields.getFields()) {
            if (field.isFocusable()) {
                fieldFirst = field;
                break;
            }
        }

        return fieldFirst;
    }

    @Override
    public DGuiField getFieldLast() {
        DGuiField fieldLast = null;

        for (int i = moFields.getFields().size() - 1; i >= 0; i--) {
            if (moFields.getFields().get(i).isFocusable()) {
                fieldLast = moFields.getFields().get(i);
                break;
            }
        }

        return fieldLast;
    }

    @Override
    public DGuiFields getFields() {
        return moFields;
    }

