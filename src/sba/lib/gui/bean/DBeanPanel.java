    @Override
    public int getPanelStatus() {
        return mnPanelStatus;
    }

    @Override
    public void setPanelSettings(DGuiClient client) {
        miClient = client;
    }

    @Override
    public void setPanelEditable(boolean editable) {
        if (editable) {
            mnPanelStatus = DGuiConsts.FORM_STATUS_EDIT;
            moFields.setFieldsEditable(true);
        }
        else {
            mnPanelStatus = DGuiConsts.FORM_STATUS_READ;
            moFields.setFieldsEditable(false);
        }
    }

